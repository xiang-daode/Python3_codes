'''
题目052_二进制位运算小结
按位与 &
按位或 |
按位异或 ^
按位取反 ~
'''
def tm052():
    '''
    【备注】：
    '''
    a=73
    b=64
    c=81

    x = bin(a)
    y = bin(b)
    z = bin(~c) #非运算(按位取反)后再转二进制
    print(x, y,z)          #0b1001001 0b1000000 -0b1010010
    # 按位运算:
    print(a & b)         #  与运算结果:64
    print(a | b)         #  或运算结果:73
    print(a ^ b)         #  异或运算结果:9

tm052()
