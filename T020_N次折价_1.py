'''
题目020：一件大衣原价500元,每年折价率85%,问25年后价格是多少?又问几年后价格降到1元？
'''
def tm020_1():
    '''
    【备注】：range()的典型应用
    '''
    m = 500
    print(1,m)
    for i in range(24):
        m = m*0.85
        print(i+2,m)

tm020_1()

def tm020_2():
    '''
    【备注】：while()的典型应用
    '''
    i=1
    m = 500
    print(1,m)
    while m>=1:
        m = m*0.85
        print(i+1,m)
        i+=1

tm020_2()
