'''
题目100：列表转换为字典。
'''
def tm100():
    '''
    【个人备注】：。
    '''
    l = ['ak17','b51','b52','#64']
    d = {}
    for i in range(len(l)):
        d[i]=l[i]
    print(d)
    # 得到：{0: 'ak17', 1: 'b51', 2: 'b52', 3: '#64'}

def tm100_1():
    # 用zip函数更简单
    l = ['ak17','b51','b52','#64']
    print(dict(zip(range(4),l)))
    # 得到：{0: 'ak17', 1: 'b51', 2: 'b52', 3: '#64'}

tm100()
tm100_1()
