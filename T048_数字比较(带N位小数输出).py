'''
题目048：数字比较。
'''
def tm048():
    '''
    【备注】：注意到print('%d 大于 %d' % (i,j))格式化的用法。
    '''
    i = 170.000001
    j = 20.000007
    if i > j:
        print('%.3f 大于 %.5f' % (i,j))#带3,5位小数
    elif i == j:
        print('%d 等于 %d' % (i,j)) #不带小数
    elif i < j:
        print('%f 小于 %f' % (i,j)) #带6位小数
    else:
        print('未知')

tm048()

