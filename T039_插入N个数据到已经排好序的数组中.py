'''
题目039：有一个已经排好序的数组aL。现输入N个人的bL，要求按原来的规律将它插入数组中。
'''
def tm039():
    '''
    【个人备注】：如N人插入到已经排序好的M人队型中,假定规律就是从小到大的。
    '''
    aL = [1,5,8,14,28,39,60,89,134,324,612,900]
    bL = [555,222,777,23,45]
    for b in bL:
        for a in aL:
            if b<a:
                aL.insert(aL.index(a),b)
                break
        else:
            aL.append(b)
    print(aL)

tm039()
