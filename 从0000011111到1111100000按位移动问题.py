#问题:有5*5格子方块,小虫从左上角开始爬,去右下角,沿着格子的线段,只能向左,向下两方向爬,
#求:共有多少种线路方案?
# 思路:记向左,向下两方向分别为'0','1',于是问题转换成二进制问题:
# 从"0000011111"到"1111100000"要经过多少次变换?每一次只移动一位'1'.
# 即:'1'必须保证有5个.

a='0000011111'
b='1111100000'

a1=int(a,2)
b1=int(b,2)

print("起点,终止,差=",a1,b1,b1-a1)
N=0
for i in range(a1,b1+1):
    sbin=bin(i)
    #print(sbin,sbin.count('1'))
    if(sbin.count('1')==5):
        print(sbin,sbin.count('1'))
        N=N+1

print("线路方案有[种]=",N)



