"""
题目047：变量或元素的位置变动。
"""
def tm047():

    # 元组中两个变量值互换
    a, b = 1, 2
    a, b = b, a
    print(a, b)

    # 列表中3个变量值互换,顺序倒置
    u = [1, 2, 3]
    print(u[::-1])

    # 元组中5个变量值互换,顺序倒置
    v = (1, 2, 3, 4, 5)
    print(v[::-1])

    # 字典中3个"键-值"对中的值互换
    w = {"A": 1, "B": 2, "C": 3}
    print(w["C"], w["B"], w["A"])

    # 字典中3个"键-值"对
    w = {"A": 1, "B": 2, "C": 3}
    print(w.values(), w.keys())


tm047()
