# 将字符串转换成元组:
tup1 = tuple("hello")
print(tup1)

# 将列表转换成元组:
list1 = ['Python', 'Java', 'C++', 'JavaScript']
tup2 = tuple(list1)
print(tup2)

# 将字典转换成元组:
dict1 = {'a' : 100, 'b' : 42, 'c' : 9}
tup3 = tuple(dict1)
print(tup3)

# 将区间转换成元组:
range1 = range(1, 6)
tup4 = tuple(range1)
print(tup4)

# 创建空元组:
print(tuple())
